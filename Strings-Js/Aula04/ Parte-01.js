/**
 * Working with String Formatting Methods
 * 
 * Parte-01: How Can you Change the Casing for a String ?
 *
 * built-in methods: toUpperCase() and toLowerCase()
 *   */ 

/**
 * 
 * toUpperCase(): converts all the characters to uppercase letters and returns a new string with all uppercase characters.
 * 
 * Example:
 * 
 */

let greeting = "Hello, World!";
let uppercaseGreeting = greeting.toUpperCase();
console.log(uppercaseGreeting);  // "HELLO, WORLD!"

/**
 * 
 * toLowerCase(): converts all the characters to uppercase letters and returns a new string with all uppercase characters.
 * 
 * Example:
 * 
 */

let shout = "I AM LEARNING JAVASCRIPT!";
let lowercaseShout = shout.toLowerCase();
console.log(lowercaseShout);  // "i am learning javascript!"
